     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if ($request->get('type') === "current") {
            $todos = Todo::where('done', false);
        } else {
            $todos = Todo::where('done', true);
        }
        return view('view-todos', [
            'todos' => $todos->get()
